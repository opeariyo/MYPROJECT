// Return a Boolean if a number is divisible by 10

function isDivisibleBy10 (number) {
    return number%10 === 0
}
console.log(isDivisibleBy10(67))
console.log(isDivisibleBy10(1000))
console.log(isDivisibleBy10(300))
console.log(isDivisibleBy10(55))


